var weekDay = 2;
switch (weekDay) {
  case 1:
    console.log("Sandy");
    break;
  case 2:
    console.log("Monday");
    break;
  case 3:
    console.log("Tuesday");
    break;
  case 4:
    console.log("Wednesday");
    break;
  case 5:
    console.log("Thursday");
    break;
  case 6:
    console.log("Friday");
    break;
  default:
    console.log("Please enter numbers 1 to 6");
}

